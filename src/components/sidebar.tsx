"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { signOut, useSession } from "next-auth/react";

const navItems = [
  { href: "/", label: "Dashboard", icon: LayoutIcon },
  { href: "/reports", label: "Reports", icon: FileIcon },
  { href: "/connections", label: "Connections", icon: DatabaseIcon },
  { href: "/schedules", label: "Schedules", icon: CalendarIcon },
  { href: "/history", label: "History", icon: ClockIcon },
];

export function Sidebar() {
  const pathname = usePathname();
  const { data: session } = useSession();

  return (
    <aside className="w-60 h-screen bg-gray-900 border-r border-gray-800 flex flex-col">
      {/* Logo */}
      <div className="p-5 flex items-center gap-3">
        <div className="w-8 h-8 rounded-lg bg-blue-600 text-white flex items-center justify-center text-sm font-bold">
          H
        </div>
        <span className="text-white font-semibold">Hermod</span>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 space-y-1">
        {navItems.map((item) => {
          const isActive =
            item.href === "/"
              ? pathname === "/"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-colors ${
                isActive
                  ? "bg-gray-800 text-white"
                  : "text-gray-400 hover:text-white hover:bg-gray-800/50"
              }`}
            >
              <item.icon className="w-4 h-4" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* User */}
      <div className="p-3 border-t border-gray-800">
        <div className="flex items-center gap-3 px-3 py-2">
          {session?.user?.image && (
            <img
              src={session.user.image}
              alt=""
              className="w-7 h-7 rounded-full"
            />
          )}
          <div className="flex-1 min-w-0">
            <p className="text-sm text-white truncate">
              {session?.user?.name}
            </p>
          </div>
          <button
            onClick={() => signOut()}
            className="text-gray-500 hover:text-gray-300 text-xs"
          >
            Sign out
          </button>
        </div>
      </div>
    </aside>
  );
}

